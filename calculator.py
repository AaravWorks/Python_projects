print("calculator")
num1 = float(input("give your first number:"))
num2 = float(input("give your second number:"))
choice = input("+,-,*,/:")
if choice == "+":
    print("the answer is:", num1 + num2)
elif choice == "-":
    print("the answer is:", num1 - num2)
elif choice == "*":
    print("the answer is:", num1 * num2)
elif choice == "/":
    print("the answer is:", num1 / num2)
    



